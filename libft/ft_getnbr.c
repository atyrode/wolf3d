#include "libft.h"

int	ft_getnbr(char *str)
{
	int i;
	int res;

	i = 0;
	res = 0;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - '0');
		i++;
	}
	if (str[0] == '-')
		res = res * (-1);
	return (res);
}
